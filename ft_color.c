int		ft_color(int t, int r, int g, int b)
{
	int		trgb;

	trgb = 0;
	if (t < 0 || t > 255 || r < 0 || r > 255
		|| g < 0 || g > 255 || b < 0 || b > 255)
		return (-1);
	trgb = t << 24 | r << 16 | g << 8 | b << 0;
	return (trgb);
}